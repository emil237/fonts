#!/bin/sh # 
 # # Command: wget https://raw.githubusercontent.com/emil237/fonts/main/installer.sh -qO - | /bin/sh # # ########################################### ###########################################  
MY_URL="https://raw.githubusercontent.com/emil237/fonts/main"
MY_PLUGIN="fonts.tar.gz"
echo "******************************************************************************************************************"
echo "    download and install fonts  "
echo "============================================================================================================================="
#####################################################################################
echo "     install plugin Fonts enigma   "
cd /tmp
set -e 
      wget "$MY_URL/$MY_PLUGIN"
wait
tar -xzf fonts.tar.gz  -C /
wait
cd ..
set +e
rm -f /tmp/fonts.tar.gz
echo "==========================================================================================================================="

echo "# Fonts  INSTALLED SUCCESSFULLY #"
echo "
echo " "*********************************************************" 
	echo "********************************************************************************"
echo "   UPLOADED BY  >>>>   EMIL_NABIL "   
sleep 4;
	echo '========================================================================================================================='
###########################################                                                                                                                  
echo ". >>>>         RESTARING     <<<<"
echo "**********************************************************************************"
wait
killall -9 enigma2
exit 0

































